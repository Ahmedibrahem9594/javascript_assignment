// declare variable
let numberOne = 10;
let numberTwo = 20;

// print normal concatenate variables 
console.log(numberOne + "" + numberTwo);

// type of normal concatenate variables
console.log(typeof (numberOne + "" + numberTwo));

//print Template Literals concatenate variables 
console.log(`${numberOne}${numberTwo}`);

// type of Template Literals concatenate variables 
console.log(typeof `${numberOne}${numberTwo}`);

// print normal concatenate variables with new line
console.log(numberTwo + "\n" + numberOne);

//print Template Literals concatenate variables  with new line
console.log(`${numberTwo} 
${numberOne}`);
